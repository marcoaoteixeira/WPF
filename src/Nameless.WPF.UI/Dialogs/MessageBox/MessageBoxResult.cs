namespace Nameless.WPF.UI.Dialogs.MessageBox;

/// <summary>
///     Represents one of the possible user dialog results.
/// </summary>
public enum MessageBoxResult {
    /// <summary>
    ///     Acknowledge result.
    /// </summary>
    Ok,

    /// <summary>
    ///     Cancel result.
    /// </summary>
    Cancel,

    /// <summary>
    ///     Accept result.
    /// </summary>
    Yes,

    /// <summary>
    ///     Deny result.
    /// </summary>
    No,
}